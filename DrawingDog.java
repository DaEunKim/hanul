package hanul;

public class DrawingDog {

	public static void main(String[] args) {
		String a = "|";
		String b = "\\";
		String c = "_";
		String d = "/";
		String q = "q";
		String p = "p";
		String o = "0";
		String e = "\"";
		String f = "^";
		String g = "=";
		String h = "}";
		String I = "(";
		String J = ")";
		String k = "`";
		System.out.println(a+b+c+d+a);
		System.out.println(a+q+" "+p+a+"   "+d+h);
		System.out.println(I+" "+o+" "+J+e+e+e+b);
		System.out.println(a+e+f+e+k+"    "+a+" ");
		System.out.println(a+a+c+d+g+b+b+c+c+a);
	}

}
